#include <stdio.h>

int main() {
  // Escreva um algoritmo para imprimir os números de 1 (inclusive) a 10 (inclusive) em ordem decrescente.

  for(int i = 10; i >= 1; i--){
    printf("Ordem decrescente 10 de %d.\n", i);
  }

  //Resultado
  //Programa funcionou corretamente imprimindo os números de 1 a 10 em ordem decrescente.  
}